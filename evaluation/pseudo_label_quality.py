"""
Pseudo-Label Quality Evaluation for Semi-Supervised Segmentation Baselines.

Evaluates the accuracy of pseudo-labels generated by UniMatch and ST++ by
comparing them against ground truth annotations. Computes per-class precision,
recall, accuracy, and generates confusion matrices to identify systematic
pseudo-label error patterns.

Author: Ebenezer Tarubinga, Korea University
"""

import argparse
import os
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import matplotlib.pyplot as plt
import numpy as np
from PIL import Image
from sklearn.metrics import confusion_matrix
from tqdm import tqdm

# Pascal VOC class names
VOC_CLASSES: List[str] = [
    "background", "aeroplane", "bicycle", "bird", "boat", "bottle", "bus",
    "car", "cat", "chair", "cow", "dining table", "dog", "horse",
    "motorbike", "person", "potted plant", "sheep", "sofa", "train", "tv/monitor"
]

IGNORE_INDEX: int = 255


def load_mask(path: str) -> np.ndarray:
    """Load a segmentation mask as a numpy array of class indices."""
    mask = np.array(Image.open(path), dtype=np.int32)
    return mask


def collect_file_pairs(
    pred_dir: str, gt_dir: str, ext: str = ".png"
) -> List[Tuple[str, str]]:
    """Find matching prediction and ground truth file pairs."""
    pred_path = Path(pred_dir)
    gt_path = Path(gt_dir)
    pred_files = sorted(pred_path.glob(f"*{ext}"))
    pairs: List[Tuple[str, str]] = []
    for pf in pred_files:
        gf = gt_path / pf.name
        if gf.exists():
            pairs.append((str(pf), str(gf)))
    return pairs


def compute_per_class_metrics(
    all_preds: np.ndarray, all_gts: np.ndarray, num_classes: int
) -> Dict[str, np.ndarray]:
    """Compute per-class accuracy, precision, and recall from flat arrays."""
    accuracy = np.zeros(num_classes, dtype=np.float64)
    precision = np.zeros(num_classes, dtype=np.float64)
    recall = np.zeros(num_classes, dtype=np.float64)
    support = np.zeros(num_classes, dtype=np.int64)

    for c in range(num_classes):
        gt_mask = all_gts == c
        pred_mask = all_preds == c
        tp = np.sum(gt_mask & pred_mask)
        fp = np.sum(~gt_mask & pred_mask)
        fn = np.sum(gt_mask & ~pred_mask)
        tn = np.sum(~gt_mask & ~pred_mask)

        support[c] = int(np.sum(gt_mask))
        accuracy[c] = (tp + tn) / (tp + tn + fp + fn) if (tp + tn + fp + fn) > 0 else 0.0
        precision[c] = tp / (tp + fp) if (tp + fp) > 0 else 0.0
        recall[c] = tp / (tp + fn) if (tp + fn) > 0 else 0.0

    return {
        "accuracy": accuracy,
        "precision": precision,
        "recall": recall,
        "support": support,
    }


def compute_confusion_matrix(
    all_preds: np.ndarray, all_gts: np.ndarray, num_classes: int
) -> np.ndarray:
    """Compute the confusion matrix, normalized row-wise."""
    cm = confusion_matrix(all_gts, all_preds, labels=list(range(num_classes)))
    row_sums = cm.sum(axis=1, keepdims=True)
    row_sums = np.maximum(row_sums, 1)  # avoid division by zero
    cm_normalized = cm.astype(np.float64) / row_sums
    return cm_normalized


def plot_confusion_matrix(
    cm: np.ndarray, class_names: List[str], title: str, save_path: str
) -> None:
    """Plot and save a heatmap-style confusion matrix."""
    fig, ax = plt.subplots(figsize=(14, 12))
    im = ax.imshow(cm, interpolation="nearest", cmap="Blues", vmin=0.0, vmax=1.0)
    fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    ax.set_title(title, fontsize=14, fontweight="bold")

    tick_marks = np.arange(len(class_names))
    ax.set_xticks(tick_marks)
    ax.set_yticks(tick_marks)
    ax.set_xticklabels(class_names, rotation=90, fontsize=8)
    ax.set_yticklabels(class_names, fontsize=8)
    ax.set_xlabel("Predicted Label", fontsize=12)
    ax.set_ylabel("Ground Truth", fontsize=12)

    # Annotate cells with values
    thresh = cm.max() / 2.0
    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            val = cm[i, j]
            if val > 0.01:
                color = "white" if val > thresh else "black"
                ax.text(j, i, f"{val:.2f}", ha="center", va="center",
                        fontsize=5, color=color)

    plt.tight_layout()
    fig.savefig(save_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved confusion matrix to {save_path}")


def plot_per_class_bar(
    metrics: Dict[str, np.ndarray],
    class_names: List[str],
    title: str,
    save_path: str,
) -> None:
    """Plot per-class precision, recall, and accuracy as grouped bar charts."""
    num_classes = len(class_names)
    x = np.arange(num_classes)
    width = 0.25

    fig, ax = plt.subplots(figsize=(16, 6))
    ax.bar(x - width, metrics["precision"], width, label="Precision", color="#2196F3")
    ax.bar(x, metrics["recall"], width, label="Recall", color="#FF9800")
    ax.bar(x + width, metrics["accuracy"], width, label="Accuracy", color="#4CAF50")

    ax.set_xlabel("Class", fontsize=12)
    ax.set_ylabel("Score", fontsize=12)
    ax.set_title(title, fontsize=14, fontweight="bold")
    ax.set_xticks(x)
    ax.set_xticklabels(class_names, rotation=60, ha="right", fontsize=8)
    ax.set_ylim(0.0, 1.05)
    ax.legend(fontsize=10)
    ax.grid(axis="y", alpha=0.3)

    plt.tight_layout()
    fig.savefig(save_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved per-class metrics plot to {save_path}")


def plot_comparison_bar(
    metrics_a: Dict[str, np.ndarray],
    metrics_b: Dict[str, np.ndarray],
    class_names: List[str],
    label_a: str,
    label_b: str,
    save_path: str,
) -> None:
    """Plot side-by-side per-class recall comparison between two methods."""
    num_classes = len(class_names)
    x = np.arange(num_classes)
    width = 0.35

    fig, ax = plt.subplots(figsize=(16, 6))
    ax.bar(x - width / 2, metrics_a["recall"], width, label=label_a, color="#1976D2")
    ax.bar(x + width / 2, metrics_b["recall"], width, label=label_b, color="#E64A19")

    ax.set_xlabel("Class", fontsize=12)
    ax.set_ylabel("Pseudo-Label Recall", fontsize=12)
    ax.set_title("Per-Class Pseudo-Label Recall: ST++ vs UniMatch", fontsize=14, fontweight="bold")
    ax.set_xticks(x)
    ax.set_xticklabels(class_names, rotation=60, ha="right", fontsize=8)
    ax.set_ylim(0.0, 1.05)
    ax.legend(fontsize=11)
    ax.grid(axis="y", alpha=0.3)

    plt.tight_layout()
    fig.savefig(save_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    print(f"  Saved comparison plot to {save_path}")


def save_metrics_csv(
    metrics: Dict[str, np.ndarray],
    class_names: List[str],
    save_path: str,
    method_name: str,
) -> None:
    """Save per-class metrics to a CSV file."""
    with open(save_path, "w") as f:
        f.write("method,class,accuracy,precision,recall,support\n")
        for i, name in enumerate(class_names):
            f.write(
                f"{method_name},{name},"
                f"{metrics['accuracy'][i]:.4f},"
                f"{metrics['precision'][i]:.4f},"
                f"{metrics['recall'][i]:.4f},"
                f"{metrics['support'][i]}\n"
            )
    print(f"  Saved metrics CSV to {save_path}")


def analyze_method(
    pred_dir: str,
    gt_dir: str,
    num_classes: int,
    method_name: str,
    class_names: List[str],
    output_dir: str,
) -> Dict[str, np.ndarray]:
    """Run full pseudo-label quality evaluation for one method."""
    print(f"\n{'='*60}")
    print(f"Analyzing pseudo-label quality: {method_name}")
    print(f"{'='*60}")

    pairs = collect_file_pairs(pred_dir, gt_dir)
    print(f"  Found {len(pairs)} image pairs")

    if len(pairs) == 0:
        print("  WARNING: No matching file pairs found. Check directory paths.")
        empty_metrics = {
            "accuracy": np.zeros(num_classes),
            "precision": np.zeros(num_classes),
            "recall": np.zeros(num_classes),
            "support": np.zeros(num_classes, dtype=np.int64),
        }
        return empty_metrics

    all_preds_list: List[np.ndarray] = []
    all_gts_list: List[np.ndarray] = []

    for pred_path, gt_path in tqdm(pairs, desc=f"  Loading {method_name}"):
        pred = load_mask(pred_path)
        gt = load_mask(gt_path)

        # Flatten and filter ignore pixels
        pred_flat = pred.flatten()
        gt_flat = gt.flatten()
        valid = gt_flat != IGNORE_INDEX
        all_preds_list.append(pred_flat[valid])
        all_gts_list.append(gt_flat[valid])

    all_preds = np.concatenate(all_preds_list)
    all_gts = np.concatenate(all_gts_list)
    print(f"  Total valid pixels: {len(all_preds):,}")

    # Overall accuracy
    overall_acc = np.mean(all_preds == all_gts)
    print(f"  Overall pseudo-label accuracy: {overall_acc:.4f}")

    # Per-class metrics
    metrics = compute_per_class_metrics(all_preds, all_gts, num_classes)
    mean_precision = np.mean(metrics["precision"][metrics["support"] > 0])
    mean_recall = np.mean(metrics["recall"][metrics["support"] > 0])
    print(f"  Mean precision: {mean_precision:.4f}")
    print(f"  Mean recall: {mean_recall:.4f}")

    # Confusion matrix
    cm = compute_confusion_matrix(all_preds, all_gts, num_classes)

    # Save outputs
    fig_dir = os.path.join(output_dir, "figures")
    tab_dir = os.path.join(output_dir, "tables")
    os.makedirs(fig_dir, exist_ok=True)
    os.makedirs(tab_dir, exist_ok=True)

    safe_name = method_name.lower().replace(" ", "_").replace("+", "p")

    plot_confusion_matrix(
        cm, class_names[:num_classes],
        f"Pseudo-Label Confusion Matrix: {method_name}",
        os.path.join(fig_dir, f"confusion_matrix_{safe_name}.png"),
    )
    plot_per_class_bar(
        metrics, class_names[:num_classes],
        f"Per-Class Pseudo-Label Quality: {method_name}",
        os.path.join(fig_dir, f"per_class_metrics_{safe_name}.png"),
    )
    save_metrics_csv(
        metrics, class_names[:num_classes],
        os.path.join(tab_dir, f"pseudo_label_metrics_{safe_name}.csv"),
        method_name,
    )

    return metrics


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Pseudo-label quality evaluation for semi-supervised segmentation baselines."
    )
    parser.add_argument(
        "--pred_dir", type=str, required=True,
        help="Directory with pseudo-label predictions (or parent with method subdirs)."
    )
    parser.add_argument(
        "--gt_dir", type=str, required=True,
        help="Directory with ground truth segmentation masks."
    )
    parser.add_argument(
        "--num_classes", type=int, default=21,
        help="Number of semantic classes (default: 21 for Pascal VOC)."
    )
    parser.add_argument(
        "--output_dir", type=str, default="results",
        help="Root directory for saving results."
    )
    parser.add_argument(
        "--compare", action="store_true",
        help="If set, expects pred_dir to contain 'unimatch/' and 'stpp/' subdirectories."
    )
    args = parser.parse_args()

    class_names = VOC_CLASSES[:args.num_classes]

    if args.compare:
        stpp_pred_dir = os.path.join(args.pred_dir, "stpp", "pseudo_labels")
        uni_pred_dir = os.path.join(args.pred_dir, "unimatch", "pseudo_labels")

        metrics_stpp = analyze_method(
            stpp_pred_dir, args.gt_dir, args.num_classes,
            "ST++", class_names, args.output_dir,
        )
        metrics_uni = analyze_method(
            uni_pred_dir, args.gt_dir, args.num_classes,
            "UniMatch", class_names, args.output_dir,
        )

        fig_dir = os.path.join(args.output_dir, "figures")
        plot_comparison_bar(
            metrics_stpp, metrics_uni, class_names,
            "ST++", "UniMatch",
            os.path.join(fig_dir, "pseudo_label_recall_comparison.png"),
        )

        # Print summary comparison
        print(f"\n{'='*60}")
        print("COMPARISON SUMMARY")
        print(f"{'='*60}")
        for i, cls_name in enumerate(class_names):
            delta = metrics_uni["recall"][i] - metrics_stpp["recall"][i]
            direction = "+" if delta > 0 else ""
            print(f"  {cls_name:>15s}: ST++={metrics_stpp['recall'][i]:.3f}  "
                  f"UniMatch={metrics_uni['recall'][i]:.3f}  "
                  f"({direction}{delta:.3f})")
    else:
        analyze_method(
            args.pred_dir, args.gt_dir, args.num_classes,
            "Method", class_names, args.output_dir,
        )

    print("\nEvaluation complete.")


if __name__ == "__main__":
    main()
